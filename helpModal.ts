import { App, Modal } from "obsidian";

export class HelpModal extends Modal {
  constructor(app: App) {
    super(app);
  }

  onOpen(): void {
    const { contentEl } = this;
    contentEl.addClass("zk-help");

    this.titleEl.setText("Ties");
    contentEl.createEl("p", {
      text: "Ты решаешь, что с чем связать, — плагин снимает механику. Он подбирает похожие по смыслу заметки, пишет типизированную связь во frontmatter, а по иконке-звену в шапке заметки показывает её связи. Тело остаётся чистой прозой.",
    });

    contentEl.createEl("h3", { text: "Как связать" });
    const ol = contentEl.createEl("ol");
    ol.createEl("li", { text: "Иконка-звено в шапке заметки → «＋ связать» (или команда / иконка слева)." });
    ol.createEl("li", {
      text: "Выбери заметку: сверху самые близкие по смыслу (% рядом), печатай для поиска.",
    });
    ol.createEl("li", {
      text: "Выбери тип (клавиши 1..9 или мышкой). Связь запишется в frontmatter текущей заметки.",
    });
    contentEl.createEl("p", {
      text: "На MOC-заметке кнопка называется «＋ участник» и работает наоборот: выбранная заметка вступает в карту (moc: пишется в ЕЁ frontmatter), тип не спрашивается.",
    });

    contentEl.createEl("h3", { text: "Типы связей" });
    const ul = contentEl.createEl("ul");
    (
      [
        ["развивает", "мысль-продолжение, углубление"],
        ["уточняет", "конкретизирует, частный случай"],
        ["противоречит", "спор, контраргумент"],
        ["пример", "иллюстрация идеи"],
        ["контекст", "источник/фон, откуда идея"],
        ["moc", "членство в карте (MOC)"],
      ] as const
    ).forEach(([t, d]) => {
      const li = ul.createEl("li");
      li.createEl("strong", { text: t + ": " });
      li.appendText(d);
    });

    contentEl.createEl("h3", { text: "Направление связи" });
    contentEl.createEl("p", {
      text: "Текущая заметка — подлежащее, поле указывает ОТ неё к цели. развивает: [[X]] = «эта заметка развивает X». В модалке: «Исходящие» = эта заметка развивает/уточняет другие; «Входящие» = другие развивают/уточняют эту заметку.",
    });

    contentEl.createEl("h3", { text: "Где смотреть связи" });
    contentEl.createEl("p", {
      text: "Иконка-звено в шапке заметки (или Cmd/Ctrl+Shift+L) открывает модалку связей, сгруппированных по типу (исходящие и входящие). У MOC-заметки видно «Участников» и «Орбиту», у обычной — строку «ближайший MOC». Есть и боковая панель «Ties: связи» (по команде).",
    });
    contentEl.createEl("p", {
      text: "Хочешь связи прямо в теле заметки — вставь блок ```ties``` (пустой). Он сам поймёт, MOC это или обычная заметка, и отрендерит соответственно.",
    });
    contentEl.createEl("p", {
      text: "У участника MOC можно дописать «почему в карте» (кнопка ✎ / ＋почему). Пояснение хранится как алиас moc-ссылки и показывается под именем участника.",
    });
    contentEl.createEl("p", {
      text: "У исходящей связи: ⇄ — сменить тип, × — убрать (с подтверждением). Входящие правятся из заметки-источника.",
    });

    contentEl.createEl("h3", { text: "Похожие (локальные эмбеддинги)" });
    contentEl.createEl("p", {
      text: "Ранжирование «похожие сверху» считает локальная модель через Ollama. Разово запусти «Переиндексировать похожие»; дальше кэш на диске и авто-обновление при правке заметки (если включено). Нужен запущенный Ollama + модель (по умолчанию bge-m3).",
    });

    contentEl.createEl("h3", { text: "Граф (команды)" });
    const gl = contentEl.createEl("ul");
    gl.createEl("li", {
      text: "Пути между заметками — топ-5 кратчайших цепочек связей от текущей до выбранной (показывает промежуточные заметки).",
    });
    gl.createEl("li", {
      text: "Все пути из заметки (обход) — дерево всех достижимых заметок (каждая один раз), сворачиваемое; переключатель «только исходящие / все связи».",
    });
    gl.createEl("li", {
      text: "Окрестность (PageRank) — что «вокруг» идеи с учётом всей структуры графа, а не только прямых ссылок.",
    });
  }

  onClose(): void {
    this.contentEl.empty();
  }
}
